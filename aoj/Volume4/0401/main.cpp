int N;
vi W, B;
vvi grundyTable(301, vi(301, -1));

int getGrundy(int w, int b){
    if(grundyTable[w][b] != -1){
        return grundyTable[w][b];
    }
    set<int> reachableGrundies;
    if(w > 0){
        reachableGrundies.insert(getGrundy(w - 1, b));
    }
    rep1(removeB, min(w, b)){
        reachableGrundies.insert(getGrundy(w, b - removeB));
    }
    if(b > 0){
        reachableGrundies.insert(getGrundy(w + 1, b - 1));
    }
    for(int g = 0;; g++){
        if(reachableGrundies.find(g) == reachableGrundies.end()){
            return grundyTable[w][b] = g;
        }
    }
}

